"""
CPV Code Classifier
-------------------
Predicts EU CPV (Common Procurement Vocabulary) codes for a tender description,
aggregated to the 4-digit division level.

Design principles:
- ZERO HALLUCINATION: predictions are ranked from the official CPV CSV only.
  No code is ever generated by a language model. The classifier is pure
  cosine similarity over a multilingual sentence-transformer embedding space.
- DETERMINISTIC: same input -> same output, every time.
- LANGUAGE-NATIVE: each tender is matched only against CPV descriptions in
  the same language (auto-detected, user-overridable). This eliminates
  cross-language noise and sharpens precision.
"""
from __future__ import annotations

import io
import re
from pathlib import Path

import numpy as np
import pandas as pd
import streamlit as st

